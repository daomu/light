/// 查询第 `step` 步的 duty 值。
///
/// - `step` ∈ 0..240：返回 `FADE_TABLE[step]`
/// - 越界：返回 0（视作已熄灭）
#[inline]
pub fn at(step: usize) -> u16 {
    if step >= FADE_TABLE.len() {
        0
    } else {
        FADE_TABLE[step]
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn endpoints() {
        assert_eq!(at(0), 1023);
        assert_eq!(at(239), 0);
    }

    #[test]
    fn out_of_range_returns_zero() {
        assert_eq!(at(240), 0);
        assert_eq!(at(usize::MAX), 0);
    }

    #[test]
    fn monotonic_nonincreasing() {
        for i in 0..FADE_TABLE.len() - 1 {
            assert!(
                FADE_TABLE[i] >= FADE_TABLE[i + 1],
                "non-monotonic at {}: {} < {}",
                i,
                FADE_TABLE[i],
                FADE_TABLE[i + 1]
            );
        }
    }
}
